// Include Guard
#ifndef __ICERAY_H__
#define __ICERAY_H__
#include "IcePoint.h"

class Ray
{
public:
	//! Constructor
	inline Ray() {}
	//! Constructor
	inline Ray(const Point &orig, const Point &dir) : mOrig(orig), mDir(dir) {}
	//! Copy constructor
	inline Ray(const Ray &ray) : mOrig(ray.mOrig), mDir(ray.mDir) {}
	//! Destructor
	inline ~Ray() {}

	Point mOrig; //!< Ray origin
	Point mDir;	 //!< Normalized direction
};

#endif // __ICERAY_H__
